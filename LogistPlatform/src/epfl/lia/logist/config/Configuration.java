package epfl.lia.logist.config;

/* import table */
import java.util.Properties;

import epfl.lia.logist.agent.AgentsetDescriptor;
import epfl.lia.logist.core.topology.TopologyDescriptor;
import epfl.lia.logist.logging.LogsetDescriptor;
import epfl.lia.logist.task.TasksetDescriptor;


/**
 * A configuration object
 *
 */
public class Configuration {
	
	/**
	 * The name of the configuration
	 */
	public String Name = null;
	
	/**
	 * The propertyset for this configuration
	 */
	public Properties Propset = null;
	
	/**
	 * The descriptor for the tasks
	 */
	public TasksetDescriptor Tasks = null;
	
	/**
	 * The descriptor for the topology
	 */
	public TopologyDescriptor Topology = null;
	
	/**
	 * The descriptor for the agents
	 */
	public AgentsetDescriptor Agents = null;
	
	/**
	 * The descriptor for the logs
	 */
	public LogsetDescriptor Logs = null;
	
	
	/**
	 * Returns a textual representation of the config.
	 */
	public String toString() {
		return "Configuration : " + Name;
	}
}
